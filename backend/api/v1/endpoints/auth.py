class ChangePasswordData(BaseModel):
    current_password: str
    new_password: str

@router.post("/change-password")
async def change_password(
    password_data: ChangePasswordData,
    token: str = Depends(oauth2_scheme),
    db: AsyncSession = Depends(get_db)
):
    """–°–º–µ–Ω–∞ –ø–∞—Ä–æ–ª—è"""
