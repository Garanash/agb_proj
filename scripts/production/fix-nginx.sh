# –ó–∞–≥—Ä—É–∂–∞–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –æ–∫—Ä—É–∂–µ–Ω–∏—è
export $(cat .env.production | grep -v '^#' | xargs)

echo "üõë –û—Å—Ç–∞–Ω–æ–≤–∫–∞ Nginx..."
docker-compose -f docker-compose.production.yml stop nginx

