    echo "‚ùå Nginx –Ω–µ –∑–∞–ø—É—Å—Ç–∏–ª—Å—è"
    echo "üìã –õ–æ–≥–∏ Nginx:"
    docker-compose -f docker-compose.production.yml logs nginx
    exit 1
